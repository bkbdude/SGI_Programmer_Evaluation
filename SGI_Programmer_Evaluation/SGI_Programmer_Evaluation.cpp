int PromblemTwoFunctionClimbingSingleThread(int factors[], int size, int place)
{
	int *multiples = new int[place];
	multiples[0] = 1; // First position is always 1.
	cout << "\nFound number : " << 1;
	int amountOfPositions = 1;
	int i = 2;

	while (amountOfPositions < place)
	{
		if (Factorization(factors, size, i))
		{
			amountOfPositions++;//If a position is found add it to multiples and add to the count.
			multiples[amountOfPositions] = i;
		}

		i++;
	}
	return multiples[place];
}

int PromblemTwoFunctionClimbingMultiThread(int factors[], int size, int place)
{
	muliThreadingClimbList.clear();
	muliThreadingClimbList.push_back(1);// the first position is always 1.
	cout << "\nFound number : " << 1;
	int i = 1;

	int batchSize = 1000;//Size of batch could be changed, I'm not sure what the optimal value would be but this seems to work well.
	int maxNumberOfThreads = thread::hardware_concurrency();
	vector<thread> t(maxNumberOfThreads);
	int numberOfPlaces = 0;

	while (numberOfPlaces < place)
	{
		for (int j = 0; j < maxNumberOfThreads; j++)
		{
			//Creating multiple thread to go one batch worth of numbers.
			t[j] = thread(CalulateBatch, factors, size, batchSize, (batchSize * j) + i);
		}

		for (int j = 0; j < maxNumberOfThreads; j++)
		{
			//Re-joining all the threads after they have all done a batch, so we can do a quick sort and check if we are done working.
			t[j].join();
		}

		muliThreadingAddList.sort();//Sorting the new posistions only. If they are added in order there is no need to sort the full list.
		muliThreadingClimbList.insert(muliThreadingClimbList.end(), muliThreadingAddList.begin(), muliThreadingAddList.end()); //Adding the short list to the long list.
		muliThreadingAddList.clear();
		i += batchSize * maxNumberOfThreads;//Setting the iterator for the next loop. Each thread checks a full batch of numbers so the next loop starting value will be much higher.
		numberOfPlaces = muliThreadingClimbList.size();
	}

	cout << "\n\n";
	for (int item : muliThreadingClimbList)//A quick output of all the positions again but sorted.
	{
		string output = "\nFinal number : " + std::to_string(item);
		cout << output;
	}

	return *next(muliThreadingClimbList.begin(), place - 1);//Returning the final position.
}

void CalulateBatch(int factors[], int size, int batchSize, int startingNumber)
{
	for (int j = 1; j <= batchSize; j++)
	{
		if (Factorization(factors, size, startingNumber + j))
		{
			muliThreadingAddList.push_back(startingNumber + j);
		}
	}
}

bool Factorization(int factors[], int size,  int value)
{
	for (int j = size - 1; j >= 0; j--)
	{
		int workingInterator = value;
		//We start with a factor see if it divides into the working Interator without remainders.
		//If it we continue divide number and try the same factor again.
		//If not we try a smaller factor.
		//If the number becomes zero then it is added to the positions.

		for (int k = j; k >= 0; k--)
		{
			int factor = factors[k];
			while (workingInterator % factor == 0)
			{
				workingInterator /= factor;
				if (workingInterator == 1)
				{
					string output = "\nFound number : " + to_string(value);
					cout << output;
					//I like the feedback for the outputs on the commmand prompt but there is no function need.
					//It just makes it easy for a user to see if the work is still being done or not.

					return true;
				}
			}
		}
	}
	return false;
}

bool CheckForDoubles(int arr[], int size)
{
	//A simple function to check if there are doubles within an array.
	for (int i = 0; i < size; i++)
	{
		for (int j = i + 1; j < size; j++)
		{
			if (arr[i] == arr[j])
			{
				return true;
			}
		}
	}				
	return false;
}

